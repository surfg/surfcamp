def main():
    for slug, fields in LANDINGS.items():
        try:
            country = Country.objects.get(slug=slug)
        except Country.DoesNotExist:
            print(f'skip: country {slug!r} not found')
            continue
        for k, v in fields.items():
            setattr(country, k, v)
        country.save()
        print(f'ok: {slug} updated')


if __name__ == '__main__':
    main()
