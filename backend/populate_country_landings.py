def main():
    # Ensure new countries exist
    for slug, defaults in NEW_COUNTRIES.items():
        country, created = Country.objects.get_or_create(
            slug=slug,
            defaults={**defaults, 'description': '', 'is_active': True},
        )
        print(f'{"created" if created else "exists"}: country {slug}')

    for slug, fields in LANDINGS.items():
        try:
            country = Country.objects.get(slug=slug)
        except Country.DoesNotExist:
            print(f'skip: country {slug!r} not found')
            continue
        for k, v in fields.items():
            setattr(country, k, v)
        country.save()
        print(f'ok: {slug} updated')


if __name__ == '__main__':
    main()
